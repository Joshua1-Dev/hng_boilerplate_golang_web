//change member route
func Member(r *gin.Engine, ApiVersion string, validator *validator.Validate, db *storage.Database, logger *utility.Logger) *gin.Engine {
	extReq := request.ExternalRequest{Logger: logger, Test: false}
	member := member.Controller{Db: db,  Logger: logger}
	_ = extReq
	memberUrl := r.Group(fmt.Sprintf("%v", ApiVersion), middleware.Authorize())
	{
		memberUrl.PUT("teams/:teamId/members/:memberId/role", member.ChangeUserRole)
	}
	return r
}